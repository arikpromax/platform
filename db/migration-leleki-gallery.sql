update public.sites
set config = jsonb_set(
  config,
  '{collections}',
  (config -> 'collections') || '{
    "key": "gallery",
    "name": "Галерея",
    "fields": [
      {"key": "title", "name": "Підпис фото", "type": "text"},
      {"key": "image", "name": "Фото", "type": "image"}
    ]
  }'::jsonb
)
where slug = 'leleki';
