--  МІГРАЦІЯ: вкладка «Галерея» для сайту Кінний двір «Лелеки»
--  Фото завантажуються через адмінку у сховище Supabase
--  і автоматично з'являються в галереї сайту (сітка + повноекранний перегляд).
